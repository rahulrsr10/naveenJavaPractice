package naveenJavaPractice;

public class PrintNosWithoutUsingNos {

	public static void main(String[] args) {
		int one='A'/'A';
		String s="..........";
		for(int i=one;i<=s.length()*s.length();i++) {
			System.out.println(i);
		}
		int d='d'; 
		for(int j=one;j<=d;j++) {
			System.out.println(j);
		}

	}

}
